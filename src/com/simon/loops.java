package com.simon;

public class loops {
    public static void main(String[] args) {
         String[] names = {
                 "Simon",
                 "James",
                 "Nadia",
                 "Sofia",
                 "Alex",
                 "Saleh"
         };

         // for loop
        for (int i = 0; i < names.length; i++) {
            System.out.println(names[i]);
        }
    }
}
